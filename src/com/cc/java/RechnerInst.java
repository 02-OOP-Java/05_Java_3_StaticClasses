package com.cc.java;

public class RechnerInst {

	public int summe(int a, int b) {
		return a+b;
	}
	
	public int differenz(int a, int b) {
		return a-b;
	}
	
	public int produkt(int a, int b) {
		return a*b;
	}

	public double quotient(double a, double b) {
		return a/b;
	}
	
}
